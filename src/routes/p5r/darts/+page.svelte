    The Darts minigame allows you to increase your baton pass rank with the
    other party members. Your baton pass rank increases the HP/SP gain and
    Damage boost that a baton pass has. In Darts, your goal is, with the
    teammate of your choosing, to reach 0. If you go over, you lose. If you go
    under after 5 rounds, you lose. It's recommended to play in a way that
    allows your teammate to finish the game because your Baton pass rank with
    them goes up twice.<br />Note about Teammate finishes: If they have an
    opportunity to win, they will ask Joker for advice. You need to choose a
    specific answer otherwise they either over or undershoot. (See table for
    correct answers)<br /><br />The billiards game isn't actually playable, BUT
    it makes technical hits so much better, its definitely worth doing. Is an
    enemy frozen or shocked? Shoot it or smash it, and it gets knocked down
    ALWAYS.<br />Quick Note: After your second/third game of billiards, the host
    will tell you about a book to "increase your skills". You need this for rank
    4, so go pick up the book from the sports store in the Underground Mall
