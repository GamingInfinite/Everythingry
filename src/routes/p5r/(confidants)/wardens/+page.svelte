Caroline and Justine
<!-- svelte-ignore a11y-missing-attribute -->
<a class="bg-black text-black hover:text-white"
  >, or Lavenza during the end of the game.</a
>
are the Velvet Room Assistants in this game. They also have several
<a class="text-blue-500" href="outings">Outings</a>
that after completing with them they reward you with skill cards.
<div class="text-red-600">
  Some Notes On Interacting With Caroline and Justine:
</div>
You're only able to START their confidant after they give you your first fusion request,
which happens on May 18th (5/18)
<br />
In order to rank them up you MUST go to the Velvet room and speak to them after fusing/summoning
the Persona they request with the correct ability on it.<br />
<!-- svelte-ignore a11y-missing-attribute -->
Outings with Caroline and Justine do NOT contribute to Confidant Rank with Strength<a
  class="bg-black text-black hover:text-white"
  >, but doing all 12 before you're locked out of them rewards you with a 13th
  with Lavenza during Third Semester</a
>
