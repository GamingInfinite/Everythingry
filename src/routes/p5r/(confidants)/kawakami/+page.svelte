Kawakami is single handedly one of the most useful confidants on first
playthrough. There are a couple things to keep note of though.<br />
Mainly the school breaks. At rank 7 she tells you that she's gonna stop being a maid
and try and get a fresh start as a teacher. The event that progresses it to rank
8 is AT school, meaning that you can ONLY get it when school is in session.<br
/>
The dates for these breaks are Summer Break on 7/26 to 8/31 and Spring Break on i
dont remember. But basically you're not able to progress until the break ends.